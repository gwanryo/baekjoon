n = int(input())
print(f"{'*' * n}{' ' * (2 * n - 3)}{'*' * n}")
for i in range(n * 2 - 3):
    if i < n - 2:
        print(f"{' ' * (i + 1)}*{' ' * (n - 2)}*{' ' * (2 * n - 2 * i - 5)}*{' ' * (n - 2)}*")
    elif i > n - 2:
        print(f"{' ' * (n * 2 - i - 3)}*{' ' * (n - 2)}*{' ' * (2 * i - 2 * n + 3)}*{' ' * (n - 2)}*")
    else:
        print(f"{' ' * (i + 1)}*{' ' * (n - 2)}*{' ' * (n - 2)}*")
print(f"{'*' * n}{' ' * (2 * n - 3)}{'*' * n}")